    <h2>Chief Superintendent Instructions</h2>

    <div class="content-section">
        <h4>1.1. INITIAL PREPARATIONS FOR THE THEORY EXAMINATION PERIOD</h4>
        <ol type="i">
            <li>The number of registered and admitted Candidates, day-wise, session-wise, shall be ascertained on the basis of the examination schedule/s.</li>
            <li>On the basis of the Number of candidates and the seating capacity of halls available, the number of examination halls required shall be fixed and necessary steps shall be taken to make arrangements for the smooth conduct of examinations.</li>
            <li>The order of appointment of invigilators/hall superintendent/s shall be issued and communicated to the invigilators by the chief superintendent of examinations of that semester.</li>
            <li>Attendance sheets/ list of registered candidates appearing in each examination hall will be prepared based on the Programme of study and Semester/ Year of study.</li>
            <li>Preparation of Answer-booklets Stock register at the examination centre will be updated after each session of examination.</li>
            <li>Specific number of Answer-booklets shall be prepared block/hall-wise. The Chief Superintendent seal shall be affixed.</li>
            <li>Seating Charts will be prepared and displayed for each examination hall.</li>
            <li>It should be verified that sufficient quantum of Question Papers of each paper have been received in sealed envelopes.</li>
        </ol>
    </div>

    <div class="content-section">
        <h4>2. DUTIES AND RESPONSIBILITIES OF CHIEF AND HALL SUPERINTENDENT/S</h4>
        <p>The CoE shall appoint one or more than one Chief Superintendent, based on the number of examinees and examination halls and communicate his/her appointment order well in advance before the commencement of examinations. Chief Superintendent(s) will not be less than the rank of Professor/Asso. Professor.</p>
        <ol>
            <li>
                <p>a) The chief superintendent is fully responsible for the smooth conduct of theory examinations. He/she has to take all pre-cautionary measures to prevent any kind of malpractice by the candidates.</p>
                <p>b) The Chief Superintendent will appoint teaching staff for invigilation work and other than that as per the approved norms of the University.</p>
                <p>c) The Chief Superintendent/ Additional Chief Superintendent and Invigilator/Hall superintendent should make it sure that no close relative of them is appearing for the University examinations.</p>
            </li>
            <li>
                <p>d) The number of Invigilator/ Hall superintendent to be appointed for the examinations should be in view of the time-table furnished by the Controller of Examinations, based on which the invigilation scheme may be prepared in advance.</p>
            </li>
            <li>
                <p>e) All teaching and non-teaching staff appointed for the conduct of the examinations will be bound and abide by the rules and regulations of the University. For any commission, omission or any act of misconduct during the process of University Examinations will be dealt with appropriate action.</p>
            </li>
            <li>
                <p>f) The Head of the department will co-ordinate the activity of practical examinations for their respective department.</p>
            </li>
            <li>
                <p>g) Chief Required Based on Students Count:</p>
                <table class="table">
                    <tr><th>No. of Studs (Per Session)</th><th>Chief Required</th><th>Additional Chief Required</th></tr>
                    <tr><td>Up to 300</td><td>1</td><td>**</td></tr>
                    <tr><td>>300 & ≤ 600</td><td>1</td><td>1</td></tr>
                    <tr><td>>600 & ≤ 900</td><td>1</td><td>2</td></tr>
                    <tr><td>>900 & ≤ 1200</td><td>1</td><td>3</td></tr>
                    <tr><td>>1200 & above</td><td>1</td><td>4</td></tr>
                </table>
            </li>
        </ol>
    </div>

    <div class="content-section">
        <h4>3. VIGILANCE</h4>
        <p>The Chief Superintendent / Additional Chief Superintendent should visit the Exam Halls as frequently as possible. He / She should also consider it as a part of his/her duty to see that the hall superintendents/invigilator keep moving among candidates and do not engage in any practice likely to diminish the efficiency of supervision they are exercising. Supervision should be very strict. Under no circumstances staff other than teaching staff be employed as hall superintendents/invigilator.</p>
    </div>

    <div class="content-section">
        <h4>4. COMMUNICATION TO COE IN CONFIDENTIAL MATTER</h4>
        <p>As all communications are confidential in nature, which should be addressed to the Controller of Examinations by mentioning the name of a person who is communicating the same.</p>
    </div>

    <div class="content-section">
        <h4>5. ROLES AND RESPONSIBILITIES OF THE COORDINATORS</h4>
        <ol>
            <li>He/she shall render all assistance to the Chief Superintendent / Additional Chief Superintendent in his/ her duties.</li>
            <li>He/she shall be present at the CS’s office 30 minutes before the commencement of examinations and till the answer sheets have taken in the safe custody of CS.</li>
            <li>He/she has to ensure the facsimile/ Signature of chief superintendent on the answer sheets.</li>
            <li>Maintaining the stock of Stationery for the examination.</li>
        </ol>
    </div>
    <div class="content-section">
        <h4>6. ROLES AND RESPONSIBILITIES OF THE ASSISTANT/S</h4>
        <ol>
            <li>Facsimile/Signature of the Chief Superintendent should be affixed only at the space provided on the title page of the main answer book.</li>
            <li>To provide the attendance sheet to the Hall superintendent/invigilator of the respective halls.</li>
            <li>To handover the attendance sheets to the Coordinators.</li>
            <li>Collection of undistributed question papers from Hall Superintendent/invigilator will be done after half an hour from the commencement of exam and will be informed to the coordinator.</li>
            <li>Assistant should enter the exam hall only in the case of emergency.</li>
        </ol>
    </div>

    <div class="content-section">
        <h4>7. PROPORTION OF HALL SUPERINTENDENT/INVIGILATOR</h4>
        <p>Normally, one hall superintendent/invigilator will be appointed for 30 students.</p>
    </div>

    <div class="content-section">
        <h4>8. INSTRUCTIONS TO HALL SUPERINTENDENTS (INVIGILATORS)</h4>
        <ol>
            <li>Reporting to CS’s office half an hour before the commencement of Examination.</li>
            <li>Verifying the blank answer sheets and the question papers while collecting it from CS/ACS for their relevance and order.</li>
            <li>Checking whether the blank answer sheets bear the facsimile of the Chief Superintendent‘s signature before issuing them to the candidates.</li>
            <li>Keep utmost vigilance in the examination hall.</li>
            <li>Preventing the candidates from indulging in any kind of malpractice/s.</li>
            <li>Bringing such malpractice (if there is any) to the immediate notice of Chief Superintendent.</li>
            <li>Instructing the candidates to complete the first page of the answer sheet, prior to the distribution of question papers.</li>
            <li>To ensure that the candidates have signed their attendance sheet.</li>
            <li>Preparing Present/Absent report.</li>
            <li>To ensure that the students have received the correct question papers.</li>
            <li>No candidates shall be allowed to enter and appear for examination after 30 minutes from the commencement.</li>
            <li>Should not allow the examinee to leave the examination hall before said the duration of the exam.</li>
            <li>To ensure that answer sheets are collected and handover properly to the CS.</li>
            <li>Returning the unused answer sheets.</li>
            <li>To extend their cooperation for the smooth conduct of examinations.</li>
            <li>Should not use or allow candidates to use Mobile or any other electronic devices during invigilation.</li>
            <li>Should not allow anyone to enter the examination hall except CS/ACS/ Co-ordinators/University officials / Squad members.</li>
            <li>To ensure that each student has mentioned the exact course code and is appearing for the expected paper; any mistake for the same he/she will be held responsible.</li>
        </ol>
    </div>

    <div class="content-section">
        <h4>9. STATIONERY ACCOUNTS</h4>
        <ol>
            <li>The Chief Superintendent and Additional Chief Superintendent should see that the coordinators are properly maintaining the account of Additional sheets/Answer sheets issued and the remaining should be tallied at the end of each exam with the help of assistant/s.</li>
            <li>The record of stationery details is subject to verification at any time from the office of CoE.</li>
        </ol>
    </div>

    <div class="content-section">
        <h4>10. DISTRIBUTION OF ANSWER SHEETS</h4>
        <p>Blank Answer sheets containing 32 pages will be supplied by the University and the Serial number of the same will also be printed on it. In case of requirement, an Additional sheet will be issued to the candidates.</p>
    </div>

    <div class="content-section">
        <h4>11. QUESTION PAPER</h4>
        <ol type="a">
            <li><strong>Verification:</strong> The Chief Superintendent should ensure that sufficient Question papers are received for all the subjects for which students have registered.</li>
            <li><strong>Safe transit:</strong> It is the responsibility of the Office of CoE to make arrangements for the safe custody and security of the Question papers.</li>
            <li><strong>Uncovering Envelope:</strong> The Question papers are to be opened only 20 minutes before the commencement of each Examination.</li>
            <li><strong>Tally of Question papers:</strong> The total number of question papers should tally with the registered candidates for each subject.</li>
            <li><strong>Distribution:</strong> The Chief superintendent should instruct hall superintendents to ensure proper distribution of appropriate question paper/s to the candidates.</li>
        </ol>
    </div>

    <div class="content-section">
        <h4>12. UNEXPECTED PUBLIC/DECLARED HOLIDAY</h4>
        <p>In case of a public holiday being after the publication of the timetable, the University Examinations will not be postponed or canceled unless otherwise notified.</p>
    </div>

    <div class="content-section">
        <h4>13. MISCONDUCT AND MALPRACTICES IN EXAMINATION BY CANDIDATES</h4>
        <p>In the Examination Hall, the candidate shall be under the disciplinary control of the Hall Superintendent/invigilator and he/ she shall obey the instructions of HS. No candidate shall use unfair means or indulge in disorderly conduct or malpractice during, or in connection with the examinations.</p>
    </div>
    <div class="content-section">
        <h4>14. IDENTIFICATION OF A CANDIDATE</h4>
        <p>The Chief Superintendent and Hall Superintendents should personally ensure the identity of candidates. The invigilator has to verify the identity of students by checking identity cards.</p>
    </div>

    <div class="content-section">
        <h4>15. CANDIDATES SUFFERING FROM INFECTIOUS DISEASES</h4>
        <p>Candidates who are suffering from infectious diseases of any kind should not be admitted to the examination hall, or else separate seating arrangements for such candidates should be made.</p>
    </div>

    <div class="content-section">
        <h4>16. SILENCE IN EXAM HALL</h4>
        <p>Strict silence should be maintained in the examination hall. This rule is applicable to both hall superintendents/invigilators as well as candidates. Smoking is strictly prohibited inside the examination hall and in the premises.</p>
    </div>

    <div class="content-section">
        <h4>17. DELAY IN DISTRIBUTION OF QUESTION PAPERS OR ANSWER SHEETS</h4>
        <p>The invigilator has to ensure that question papers and answer sheets are distributed on time. Candidates shall not be allowed to write answers beyond the prescribed time limit for the subject concerned. However, if there is a delay caused in the distribution, examinees should be allowed extra time equal to the delay.</p>
    </div>

    <div class="content-section">
        <h4>18. SEATING ARRANGEMENTS</h4>
        <p>Seating arrangements in every examination hall must be meticulously planned so that candidates appearing for the same subject may not sit together on one bench. Not more than 50 percent of the seating capacity of a hall shall be allotted for candidates appearing for one particular subject. The Chief Superintendent should ensure comfortable and proper seating arrangements. If all examinees in one session are appearing for only one subject, the Chief Superintendent should ensure a minimum distance of one meter between candidates.</p>
    </div>

    <div class="content-section">
        <h4>19. CANDIDATES SHOULD OCCUPY ONLY ALLOTTED SEAT</h4>
        <p>No candidate is permitted to occupy any seat other than the allotted one. Altering the seating arrangement without concurrence of the Chief Superintendent may result in cancellation of that particular session’s written examination.</p>
    </div>

    <div class="content-section">
        <h4>20. CANDIDATE TO OCCUPY SEATS</h4>
        <p>Candidates shall occupy their seats at least ten minutes prior to the commencement of the examination. They will not be allowed to leave their seats under any pretext during examination hours.</p>
    </div>

    <div class="content-section">
        <h4>21. ENTERING AND LEAVING HALLS</h4>
        <p>No candidate shall be allowed to enter the examination hall half an hour after the commencement of the exam and leave the hall before 2.5 hours.</p>
    </div>

    <div class="content-section">
        <h4>22. SEPARATE ANSWER SHEETS FOR ACCOUNTANCY SUBJECT</h4>
        <p>Candidates appearing for accountancy subjects will be supplied separate answer sheets.</p>
    </div>

    <div class="content-section">
        <h4>23. USE OF WRITING MATERIAL</h4>
        <p>Candidates should carry their own writing material, such as pens and pencils, as they will not be allowed to borrow from others in the examination hall. Only blue or black pens are allowed for writing. Colors may be used for drawing diagrams or charts.</p>
    </div>

    <div class="content-section">
        <h4>24. IS CODE BOOKS / DATA BOOKS / TABLES</h4>
        <p>IS code books, Clark’s Mathematical & Physical Data Book / Tables, and other scientific Tables / Data books, if mentioned in the question paper, will be supplied to candidates upon request by the hall superintendent.</p>
    </div>

    <div class="content-section">
        <h4>25. CALCULATORS</h4>
        <p>The use of mathematical instruments, such as scientific calculators, is allowed as per subject requirements. However, programmable calculators, cell phones, and pagers are not allowed.</p>
    </div>

    <div class="content-section">
        <h4>26. TO KEEP BOOKS, NOTEBOOKS OUTSIDE</h4>
        <p>All books, notebooks, manuscripts, etc., brought by candidates shall be kept outside of the examination hall.</p>
    </div>

    <div class="content-section">
        <h4>27. CANDIDATES ARE NOT ALLOWED TO ASK ANY QUESTIONS</h4>
        <p>Candidates are forbidden from asking any questions during the examination. Hall superintendents are instructed not to answer any inquiries whatsoever regarding the question papers.</p>
    </div>

    <div class="content-section">
        <h4>28. ATTENDANCE OF CANDIDATE</h4>
        <p>The Hall Superintendents/invigilator will take attendance after ten minutes of the commencement of the examination, getting signatures of candidates and completing within 30 minutes.</p>
    </div>

    <div class="content-section">
        <h4>29. ABSENTEE STATEMENT</h4>
        <p>The ABSENTEE STATEMENT should be collected after half an hour of the commencement of Examination (in each session) in the prescribed format.</p>
    </div>

    <div class="content-section">
        <h4>30. CONSOLIDATED ABSENTEE STATEMENT</h4>
        <p>The Chief Superintendent is expected to send a consolidated absentee statement, date and subject-wise, and a list of absentees at the end of each examination.</p>
    </div>

    <div class="content-section">
        <h4>31. PREPARATION OF ANSWER PAPER COVER</h4>
        <p>Special care must be taken to ensure accuracy in writing the correct PRN of absentees on the cover.</p>
    </div>

    <div class="content-section">
        <h4>32. COLLECTION OF ANSWER PAPERS</h4>
        <p>Hall superintendent/invigilator should verify the details of the candidate at the time of collection of answer sheets.</p>
    </div>

    <div class="content-section">
        <h4>33. ARRANGING OF ANSWER SHEETS</h4>
        <p>Answer sheets should be carefully arranged as per the register number.</p>
    </div>

    <div class="content-section">
        <h4>34. ENTRIES ON THE ANSWER PAPER COVER</h4>
        <p>The register number of all candidates in the nominal roll shall be written on the left side of the cover in the space provided. Absentees may be notified on the right side of the cover.</p>
    </div>

    <div class="content-section">
        <h4>35. SIGNATURE OF THE CHIEF SUPERINTENDENT</h4>
        <p>The signature of the Chief Superintendent, name, and seal of the University shall be affixed on the cover.</p>
    </div>

    <div class="content-section">
        <h4>36. SEALING OF ANSWER PAPER COVERS</h4>
        <p>No page should be torn off from the answer books. The number of answer books kept inside the cover shall not exceed the specified number indicated thereon.</p>
    </div>

    <div class="content-section">
        <h4>37. DISPATCH OF ANSWER SCRIPTS</h4>
        <p>The Chief Superintendent is responsible for the dispatch of answer sheets, unused Question Paper covers, and other materials as and when the examination is over on the same day to the CAP office. Failure to dispatch will entail action.</p>
    </div>

    <div class="content-section">
        <h4>38. DISPATCH OF ATTENDANCE SHEET AND ABSENTEES STATEMENT</h4>
        <p>The Chief Superintendent is responsible for dispatching the attendance sheet and absentee statement as and when the examination is over, on the same day, through assistants.</p>
    </div>

    <div class="content-section">
        <h4>39. MALPRACTICE</h4>
        <p>The answer sheet of any candidate involved in malpractice should be sent to the Controller of Examinations separately.</p>
    </div>

    <div class="content-section">
        <h4>40. INSTRUCTION TO THE CANDIDATES</h4>
        <p>Candidates shall be asked to keep electronic devices such as calculators, digital diaries, mobile phones, pagers, or any other communication devices out of the examination hall before entering it. Candidates must show their Identity card for mid-semester and hall ticket for end-semester examinations on demand.</p>
    </div>

    <div class="content-section">
    <h4>3. EXAMINATION SESSION TIMINGS AND BELL ALERTS</h4>
    <p>Unless otherwise notified, the forenoon and afternoon sessions of theory examination shall be as follows:</p>
    <ul>
        <li>Fore Noon (FN): 9:30 a.m. to 12:30 p.m.</li>
        <li>After Noon (AN): 2:00 p.m. to 5:00 p.m.</li>
    </ul>
    <p>The schedule/s for practical examinations to be conducted in the specified laboratories of the departments shall be notified on the examination notice boards of the respective departments at least a week in advance.</p>
    <p>In an examination session, there will be 7 Bell alerts. The scheme of ringing of the bell during the examination session and the operations linked with it are given below:</p>
    <ul>
        <li><b>First Long Bell:</b> 10 minutes before the commencement of the Examination</li>
        <ul>
            <li>Candidates will enter the examination hall and take their respective seats.</li>
            <li>Answer-booklets will be distributed.</li>
        </ul>
        <li><b>Second Long Bell:</b> Scheduled start-time of the examination session</li>
        <ul>
            <li>Start of Examination.</li>
            <li>Question papers will be distributed.</li>
            <li>No Candidate should be allowed to leave the examination hall until the next bell.</li>
        </ul>
        <li><b>Third Bell:</b> One Stroke at the end of the first 30 minutes of examination.</li>
        <ul>
            <li>HS shall also make the consolidated attendance sheet of the Hall.</li>
            <li>All the absentees should be marked “ABSENT” using a RED ink pen in the attendance sheet/register.</li>
            <li>No candidate should be allowed to enter the examination hall.</li>
        </ul>
        
        <li><b>Fourth Bell:</b> Two Strokes at the end of 60 minutes of examination.</li>
        
        <li><b>Fifth Bell:</b> Three Strokes at the end of 120 minutes of examination.</li>
        
        <li><b>Sixth Bell:</b> One Short Stroke bell at the end of 170 minutes of examination.</li>
        <ul>
            <li>Warning 10 minutes before the end of the examination.</li>
            <li>Candidates are alerted about the remaining time.</li>
            <li>Candidates are reminded to enter the total pages written and strike off the unwritten blank pages in the answer booklet.</li>
        </ul>

        <li><b>Seventh Bell:</b> One Long Bell at the end of 180 minutes of examination.</li>
        <ul>
            <li>End of Examination.</li>
            <li>Answer papers are collected.</li>
        </ul>
    </ul>
    <p class="note">Note: The ringing of bells will be according to the time indicated by the watch/clock in the Examination Control Room (ECR).</p>
</div>

<div class="content-section">
    <h4>4. EXAMINATION CONTROL ROOM (ECR) AT THE EXAMINATION CENTRE</h4>
    <p>The ECR room shall function as the Office at the Examination centre. Refer to Guidelines for the functions of ECR in Annexure – I.</p>
</div>

<div class="content-section">
    <h4>5. MISCONDUCT AND MALPRACTICES IN EXAMINATION BY CANDIDATES</h4>
    <p>In the Examination Hall, the candidate shall be under the disciplinary control of the Hall Superintendent/invigilator and must obey the instructions of HS. No candidate shall use unfair means or indulge in disorderly conduct or malpractice during, or in connection with, the examinations.</p>
</div>

<div class="content-section">
    <h4>11.1 MISCONDUCT</h4>
    <p>A candidate indulging in any of the following acts in the examination hall will be considered as Misconduct in the examination and booked for the same.</p>
    <ul>
        <li>Candidate blatantly disobeying instructions of the Chief Superintendent (CS) / Hall Superintendent (HS) / Vigilance squad member.</li>
        <li>Exhibiting insolent/violent behavior with HS or other examination staff or other examinees.</li>
        <li>Acts of threats, violence, creating disturbance inside or outside the examination hall.</li>
        <li>Bringing dangerous weapons or articles into the examination center on any pretext.</li>
        <li>Any acts by candidates that hinder the discharge of duty by staff on examination duty or the smooth conduct of the examination.</li>
        <li>Tearing off or mutilating an answer booklet or any part thereof.</li>
        <li>Talking with neighbors, showing signs or signals to others in the hall or verandah.</li>
        <li>Writing the examination even after the final bell despite several calls to stop writing.</li>
        <li>Causing damage to the belongings of Examiners, as an act of vengeance.</li>
        <li>Arranging outside persons to terrorize the examination staff.</li>
        <li>Causing damage to examination material or records.</li>
        <li>Assaulting physically or attempting to attack any examination staff while being caught/booked for any malpractice.</li>
        <li>Writing filthy/indecent/vulgar words or sketching obscene figures in the answer booklet, on blackboards, walls, or furniture at the examination center.</li>
        <li>Tampering with the particulars, including PRN, written on another candidate’s answer book, and/or writing wrong particulars, including PRN on one’s own answer-booklet deliberately.</li>
        <li>Refusing to be searched by the HS/CS or any other member of the supervisory staff, or obstructing or hindering such search in the Examination Hall, Verandah, etc.</li>    </ul>
</div>

<div class="content-section">
    <h4>11.2 MALPRACTICES</h4>
    <p>A candidate indulging in any of the following acts in the examination hall will be considered as committing malpractice(s) in the examination and booked for the same:</p>
    <ul>
        <li>Possession of printed/photocopy/manuscripts pertaining/not pertaining to the day’s examination, or in the form of book, pages of a book or loose sheets, chits, or any other matter not permissible to be brought into the Examination hall.</li>
        <li>Possession of Tables, Handbooks, Code books, etc., which are not permitted for the examination.</li>
        <li>Possession of Programmable Calculators, Mobile Phones, Smartwatches/Bluetooth or Wi-Fi-enabled devices, Pagers, Organizers, Digital memory pad, etc., with/without matters related to the examination stored in them.</li>
        <li>Possessing any other prohibited material that may be of help in writing answers or facilitate copying in the examination hall.</li>
        <li>Writing any matter, formula, definitions, sketches on any part of the candidate’s body, dress, handkerchief, instrument box, calculator cover, wall, table, drawing board, Question paper, hall ticket, etc., which may have relevance to the syllabus of the examination-paper concerned.</li>
        <li>Exchanging of hall tickets, question papers, scales, calculator, instrument box, etc., with any formula, answers, sketches, and hints related to examination written on them with other examinee.</li>
        <li>Showing the answer-booklet/question paper to another examinee or allowing them to copy.</li>
        <li>Giving/Receiving the main answer-booklet or other information-carrying material to/from another examinee for copying.</li>
        <li>Copying from any printed or hand-written material or answer-booklet of another candidate.</li>
        <li>Writing any appeal to the Examiner offering money or any irrelevant matters in the answer script, inserting currency notes in answer script, and giving contact address, telephone numbers, etc.</li>
        <li>Communicating or attempting to communicate, directly or through another person with an examiner or with an official of the University with the intent of influencing him in the award of marks or making any approach or manipulation for that purpose.</li>
        <li>Communicating or attempting to communicate with the staff of the University or any person connected with the conduct of examination or with any paper-setter, with the aim of discovering the name and address of the paper setter or learning the questions.</li>
        <li>Making any request or representation or offering any threat or inducement to Supervisor/Room Superintendent or any other official/officer of the University/College in the Examination Hall or to the Examiner in the answer sheets.</li>
        <li>Approaching directly or indirectly the teachers, Officers or Examiners to bring about undue pressure or influence for favour in the examination or valuation.</li>
        <li>Writing the register number anywhere in the answer-booklet other than in the fly slip of the Front Page.</li>
        <li>Revealing identity in any form in the answer written or any part of the answer-booklet during the University examination.</li>
        <li>Writing the examination without a valid hall ticket or obtaining admission to the examination on a false representation.</li>
        <li>Striking off all the pages, including correct answers written by him/her in the answer-booklet, when caught for any act of malpractice.</li>
        <li>Inserting pre-written additional sheet(s) having matters related to the subject of the examination into the main answer-sheet(s).</li>
        <li>Smuggling in a blank or written answer-booklet and forging the Supervisor's signature.</li>
        <li>Replacing the answer-booklet supplied in the hall with a pre-prepared answer-booklet brought by the candidate or obtained from external sources.</li>
        <li>Presenting a practical or class-work notebook to the examiner that does not belong to the candidate.</li>
        <li>Forging the teacher's signature anywhere in the Practical Record book.</li>
        <li>Altering the marks awarded for exercises in the Practical Record book or boosting the total and average marks obtained in the Record book of Laboratory work.</li>
        <li>Helping another candidate answer any question orally or by transmission of material or information.</li>
        <li>Giving or receiving assistance in answering the question papers to or from any other candidate/person inside or outside the examination hall during examination hours.</li>
        <li>Unauthorized communication in any form with other examinees or anyone else inside or outside the examination hall made deliberately to create confusion.</li>
        <li>Mutual/Mass copying by candidates in the examination hall.</li>
        <li>Impersonation - Arranging for someone else to write the examination for the candidate (or attempting to write the examination for someone else).</li>
        <li>Taking away the Answer script without handing it over to the Hall Superintendent or attempting to take away or damage another candidate’s answer-booklet/sheet.</li>
        <li>Refusing to provide a written statement, refusing to receive a show-cause notice, absconding from inquiries, running away from the examination hall when caught for any malpractice, trying to destroy evidence by tearing/swallowing/erasing/throwing away bits, materials, etc., or causing the disappearance of any such material.</li>
        <li>Interfering with or counterfeiting the University/Institution seal or answer-booklets or office stationery used in the examinations.</li>
        <li>Making a false representation regarding the eligibility of the candidate to appear in the examination.</li>
        <li>Committing any act(s) to gain an advantage or favour in the examination by misleading, deceiving, or inducing the examiner or university authorities.</li>
        <li>Forging another person’s signature in any of the examination documents.</li>
        <li>Any other similar act(s) of commission or omission that may be considered as Malpractice by the competent authority.</li>
    </ul>
</div>

