    <h4>11.3 HANDLING AND REPORTING MISCONDUCT, MALPRACTICE AT EXAMINATIONS</h4>
    <p>The following procedures shall be followed for dealing with cases of candidates found using or suspected of using unfair means or involved in malpractice or misconduct in connection with the examinations:</p>
    <ul>
        <li>Where a candidate is suspected of using unfair means as defined above, the HS or the centre superintendent or any other member of the supervisory staff or observer shall search the candidate and/or his belongings. Where any written or printed material is found in his possession as a consequence of the search, the matter is taken to the notice of the Chief Superintendent of Examination centre. The candidate’s answer booklet shall be confiscated along with the material recovered.</li>
        
        <li>The Chief Superintendent shall hold a preliminary enquiry, record the report of the Hall Superintendent, and the statement of the candidate in the presence of a teacher other than the HS concerned. On sufficient grounds, the Chief Superintendent shall forthwith suspend the candidate from writing the examination and withdraw the hall ticket of the examinee.</li>
        
        <li>The Chief Superintendent shall thereafter obtain a statement/undertaking of the student. The HS shall provide his/her report. CS shall countersign both the report and the statement. The candidate shall be made to affix his/her signature on the incriminating materials used in the malpractice. Answer scripts of all such cases are packed separately.</li>
        
        <li>If the candidate refuses to give a statement, he/she should be asked to record in writing his/her refusal to give a statement. If he/she refuses to give a statement, the facts shall be noted duly witnessed by two members of the supervisory staff.</li>
        
        <li>If the candidate refuses to hand over the incriminating material or destroys or runs away with the material (causes to disappear), the facts shall be noted duly witnessed by two members of the supervisory staff and the matter reported to the Chief Superintendent immediately.</li>
        
        <li>In case of Misconduct/insolent behaviour (i.e., verbal or nonverbal, e.g., gestures), the Supervisor or concerned authorized person shall record the facts in writing and shall report the same to the Controller of Examinations.</li>
        
        <li>The answer scripts, relevant question paper(s), statement of the errant-examinee, and the incriminating materials confiscated shall be packed in a cover and labelled. This packet in turn shall be placed in an outer cover along with the HS Report(s) of all such cases along with the consolidated report of the CS on cases of malpractices for the session. It is reiterated that these Answer scripts should NOT be packed with the other Answer scripts. They should be placed in a separate packet and sealed. It is the responsibility of the Centre’s Chief Superintendent to ensure that the malpractice related bundle should be safely handed over to the CoE with appropriate labelling.</li>
        
        <li>A record of such malpractice shall be maintained at the examination centre in order to prevent such candidates from appearing in subsequent examinations by pretending to have lost the Hall ticket.</li>
        
        <li>The Office of the CoE shall open only the outer cover containing the reports of HS/CS and the inner cover/packet containing the malpractice-related answer scripts, statement of the errant-examinee, and the incriminating materials confiscated shall not be opened.Based on the reports of HS/CS, the CoE shall place such cases before the Malpractice Committee (MC) along with all the materials and records received by him/her.</li>
        
        <li>Post-Examination, if the examiner at the time of assessment of answer-booklet(s) suspects that there is prima facie evidence that the examinee(s) whose answer script(s) the examiner is assessing appears to have resorted to malpractice, he/she shall forthwith stop further evaluation and return the answer script with a report prepared by him under his/her name and signature to the CAP director in a separate confidential sealed envelope marked as “Suspected Malpractice case.” The CAP director shall forward the same to the CoE along with his/her remarks and signature. If the suspicion arises or malpractice is suspected after the script is already valued, marks shall not be entered in the marks list along with other students but shall enter them in a separate list and hand over to the custodian who in turn shall forward the same to the CoE in a sealed cover.</li>
    </ul>
</div>
